import streamlit as st

def render_financial_planning():
    """STUCO Constitution Module"""
    st.subheader("📜 STUCO Constitution")
    st.markdown("---")

    # Article I
    st.markdown("### **Article I – Mission**")
    st.write(
        "The mission of the SCIS High School Student Council is to represent the student body, advocate for student needs, "
        "and act as a catalyst for positive change within our school. We achieve this by developing student leaders, "
        "orchestrating events, and collaborating with faculty and administration teams to uphold the excellence and values of SCIS, "
        "as well as the core values of the Student Council: community, integrity, respect, leadership, and growth."
    )
    st.markdown("---")

    # Article II
    st.markdown("### **Article II – Vision**")
    st.write(
        "To be the authentic and representative voice of the student body and to build a more inclusive, connected, and spirited school community."
    )
    st.markdown("---")

    # Article III
    st.markdown("### **Article III – STUCO Values**")
    st.write("1. Community – inclusivity, collaboration, service")
    st.write("2. Integrity – Act honestly, ethically, and with accountability")
    st.write("3. Respect – value different perspectives and treat others with dignity")
    st.write("4. Leadership – Inspire peers through initiative and responsibility")
    st.write("5. Growth – aim to grow personally")
    st.markdown("---")

    # Article IV
    st.markdown("### **Article IV – Leadership**")
    st.write(
        "The high school student council has student leaders from the grade 11 cohort (leaders are picked at the end of each academic year), "
        "who work alongside the faculty advisors for HS STUCO as well as the principal, activities coordinator, and operations team."
    )
    st.markdown("---")

    # Article V
    st.markdown("### **Article V – Membership**")
    st.write(
        "Any student from grade 9-11 at SCIS HQ is eligible to apply for student council. Each applicant, however, must go through an application process "
        "wherein they fill a form showing their interest in STUCO after which they must submit a one paged resume outlining their academic achievements, "
        "extracurriculars, other skills, and personal interests."
    )
    st.markdown("---")

    # Article VI
    st.markdown("### **Article VI – Acceptance Criteria with Weightage in (%)**")
    st.write(
        "Applications for student council will be reviewed by leaders and faculty advisors. When reviewing applications, the following should be considered: "
        "any kind of leadership experience (minor or major), average to above average academic performance, community engagement shown through 2 or more activities, "
        "willing to display commitment and dedication to STUCO."
    )
    st.markdown("---")

    # Article VII
    st.markdown("### **Article VII – Roles within STUCO**")
    st.write(
        "Student council will be broken up into sub committees or focus groups wherein they will focus on one aspect of the council. "
        "This could include a social media team, marketing team, event planning team, and so on."
    )
    st.markdown("---")

    # Article VIII
    st.markdown("### **Article VIII – Electing New Leaders**")
    st.write(
        "An election process will take place at the end of every academic year where students going into grade 11 can run to be leader for student council. "
        "To do so they must deliver a speech to everyone in STUCO after which voting will take place. Each member vote counts as one vote toward the students’ total tally "
        "whereas faculty advisor, leader, and admin votes count as two toward the total tally."
    )
    st.markdown("---")

    # Article IX
    st.markdown("### **Article IX – General Guidelines**")
    st.write("1. Members are not allowed to use phones while STUCO is in session")
    st.write("2. Always be respectful")
    st.write("   a. There are no bad ideas so do not disrespect or diminish your peers if they suggest something. There is no tolerance for disrespect toward each other in STUCO")
    st.write("3. Do not volunteer to skip a part of class more than once a month")
    st.write("4. If you volunteer during class, you must inform your teachers in advance.")
    st.write("5. Be original. We want to see your ideas. Not ChatGPT’s")
    st.markdown("---")

    # Article X
    st.markdown("### **Article X – Expectations**")
    st.write("1. Attend all STUCO meetings through the year unless excused by the leaders or a HS STUCO faculty advisor")
    st.write("2. Communicate with at least one of the leaders or a faculty advisor if you are unable to make it to a meeting due to sickness or another commitment on campus. Failure to do so will count as an unexcused absence")
    st.write("3. Be an active participant within discussions")
    st.write("4. Volunteer time to help run student council events at least xyz times per semester")
    st.markdown("---")

    # Article XI (Meetings)
    st.markdown("### **Article XI – Meetings**")
    st.write("1. High school student council meets once every week on Monday")
    st.write("2. Meetings start at 1:15, so please be there before then. Arriving after 1:20 Results in a late mark")
    st.write("3. Meetings are held in Mr. Iacobucci’s room in C020?")
    st.write("4. The meeting could be cancelled due to several factors including but not limited to:")
    st.write("   a. End of semester exams for the leaders")
    st.write("   b. Faculty advisors not being available at the specified time")
    st.write("   c. Other school events")
    st.write("   d. Hyper block for the leaders")
    st.markdown("---")

    # Article XI (Removal)
    st.markdown("### **Article XI – Removal from STUCO**")
    st.write("1. Members are given three unexcused absences per year (the first absence results in a reminder from STUCO leaders to attend meetings, the second results in an email from the STUCO leaders with faculty advisors cc’d, with the third resulting in their removal from student council)")
    st.write("2. Not respecting or being dismissive of other members within the council")
    st.write("3. Not keeping up with classwork")
    st.write("4. Using STUCO as a justification for missed assignments")
    st.write("5. Members who consistently fail to engage in projects, even if attending meetings, may receive a strike.")
    st.write("6. Not being an active member of the council during meetings and/or events. This is determined by seeing how engaged the participants are during discussions and events.")
